package com.hoyotech.prison.bean;

import java.util.Date;

public class BasicSetUp {
	private String id;     
	private Integer setup_type;   //设置类型
	private String setup_title;   //设置标题
	private Integer ischain;      //是否设置链接
	private Integer istop;        //是否已经设置
	private String chain_adress;   //链接地址
	private Date addtime;      //添加时间
	
	
	public Date getAddtime() {
		return addtime;
	}
	public void setAddtime(Date addtime) {
		this.addtime = addtime;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public Integer getSetup_type() {
		return setup_type;
	}
	public void setSetup_type(Integer setupType) {
		setup_type = setupType;
	}
	public String getSetup_title() {
		return setup_title;
	}
	public void setSetup_title(String setupTitle) {
		setup_title = setupTitle;
	}
	public Integer getIschain() {
		return ischain;
	}
	public void setIschain(Integer ischain) {
		this.ischain = ischain;
	}
	public Integer getIstop() {
		return istop;
	}
	public void setIstop(Integer istop) {
		this.istop = istop;
	}
	public String getChain_adress() {
		return chain_adress;
	}
	public void setChain_adress(String chainAdress) {
		chain_adress = chainAdress;
	}
	
	

}
