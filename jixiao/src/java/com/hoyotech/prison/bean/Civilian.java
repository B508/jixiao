package com.hoyotech.prison.bean;

import java.util.Date;

import com.hoyotech.prison.log.PropertyName;

public class Civilian {

	@PropertyName(name = "ID")
	private String id;// 主键
	@PropertyName(name = "姓名")
	private String name; // 姓名
	@PropertyName(name = "性别")
	private Dictionary gender; // 性别
	@PropertyName(name = "出生日期")
	private Date birthday; // 出生日期
	@PropertyName(name = "身份证号")
	private String idnumber; // 身份证号
	@PropertyName(name = "民族")
	private Peoples peoples; // 民族
	@PropertyName(name = "政治面貌")
	private Dictionary politicalStatus; // 政治面貌
	@PropertyName(name = "学历")
	private Dictionary eduBackground; // 学历
	@PropertyName(name = "婚姻状况")
	private Dictionary marryStatus;// 婚姻状况
	@PropertyName(name = "居住地址")
	private String address; // 居住地址
	@PropertyName(name = "联系地址")
	private String phoneNum; // 联系方式
	@PropertyName(name = "毕业学校")
	private String graduate; // 毕业学校
	@PropertyName(name = "毕业时间")
	private Date graduateTime; // 毕业时间
	@PropertyName(name = "专业")
	private String profession; // 专业
	private int state = 1;
	private String prisonCode;
	private Date addTime;
	private Date updateTime;

	private int isSync = 0;// 是否同步；0：未同步；1：已同步

	public Civilian() {

	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public int getIsSync() {
		return isSync;
	}

	public void setIsSync(int isSync) {
		this.isSync = isSync;
	}

	public Dictionary getMarryStatus() {
		return marryStatus;
	}

	public void setMarryStatus(Dictionary marryStatus) {
		this.marryStatus = marryStatus;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getPhoneNum() {
		return phoneNum;
	}

	public void setPhoneNum(String phoneNum) {
		this.phoneNum = phoneNum;
	}

	public String getGraduate() {
		return graduate;
	}

	public void setGraduate(String graduate) {
		this.graduate = graduate;
	}

	public Date getGraduateTime() {
		return graduateTime;
	}

	public void setGraduateTime(Date graduateTime) {
		this.graduateTime = graduateTime;
	}

	public String getProfession() {
		return profession;
	}

	public void setProfession(String profession) {
		this.profession = profession;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Dictionary getGender() {
		return gender;
	}

	public void setGender(Dictionary gender) {
		this.gender = gender;
	}

	public Date getBirthday() {
		return birthday;
	}

	public void setBirthday(Date birthday) {
		this.birthday = birthday;
	}

	public String getIdnumber() {
		return idnumber;
	}

	public void setIdnumber(String idnumber) {
		this.idnumber = idnumber;
	}

	public Peoples getPeoples() {
		return peoples;
	}

	public void setPeoples(Peoples peoples) {
		this.peoples = peoples;
	}

	public Dictionary getPoliticalStatus() {
		return politicalStatus;
	}

	public void setPoliticalStatus(Dictionary politicalStatus) {
		this.politicalStatus = politicalStatus;
	}

	public Dictionary getEduBackground() {
		return eduBackground;
	}

	public void setEduBackground(Dictionary eduBackground) {
		this.eduBackground = eduBackground;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}

	public String getPrisonCode() {
		return prisonCode;
	}

	public void setPrisonCode(String prisonCode) {
		this.prisonCode = prisonCode;
	}

	public Date getAddTime() {
		return addTime;
	}

	public void setAddTime(Date addTime) {
		this.addTime = addTime;
	}

	public Date getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}

}
