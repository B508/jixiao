package com.hoyotech.prison.bean;

import java.util.Date;

public class Comment {
	
	private String comment_id;           //评论ID
	private String comment_userid;       //评论人ID
	private String commentby_userid;     //被评论人ID
	private String comment_content;      //评论内容
	private Date comment_time;         //评论时间
	public String getComment_id() {
		return comment_id;
	}
	public void setComment_id(String commentId) {
		comment_id = commentId;
	}
	public String getComment_userid() {
		return comment_userid;
	}
	public void setComment_userid(String commentUserid) {
		comment_userid = commentUserid;
	}
	public String getCommentby_userid() {
		return commentby_userid;
	}
	public void setCommentby_userid(String commentbyUserid) {
		commentby_userid = commentbyUserid;
	}
	public String getComment_content() {
		return comment_content;
	}
	public void setComment_content(String commentContent) {
		comment_content = commentContent;
	}
	public Date getComment_time() {
		return comment_time;
	}
	public void setComment_time(Date commentTime) {
		comment_time = commentTime;
	}
	
	
}
