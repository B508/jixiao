package com.hoyotech.prison.bean;

import java.util.Date;

import com.hoyotech.prison.log.PropertyName;

public class ContrabandGoods {

	@PropertyName(name = "ID")
	private String id;
	private Prisoner prisoner;// 被拘留人
	@PropertyName(name = "编号")
	private String contrabandGoodsNumber;// 编号
	@PropertyName(name = "名称")
	private String contrabandGoodsName;// 名称
	@PropertyName(name = "规格")
	private String contrabandGoodsNorms;// 规格
	@PropertyName(name = "数量")
	private String contrabandGoodsQuantity;// 数量
	@PropertyName(name = "特征")
	private String contrabandGoodsFeature;// 特征
	@PropertyName(name = "备注")
	private String contrabandGoodsRemark;// 备注
	private int state = 1; // 状态
	private Date addTime;
	private Date updateTime;
	private String prisonCode;
	private int isSync = 0;// 是否同步；0：未同步；1：已同步

	/** default constructor */
	public ContrabandGoods() {
	}


	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public int getIsSync() {
		return isSync;
	}

	public void setIsSync(int isSync) {
		this.isSync = isSync;
	}

	public Prisoner getPrisoner() {
		return this.prisoner;
	}

	public void setPrisoner(Prisoner prisoner) {
		this.prisoner = prisoner;
	}

	public String getContrabandGoodsNumber() {
		return this.contrabandGoodsNumber;
	}

	public void setContrabandGoodsNumber(String contrabandGoodsNumber) {
		this.contrabandGoodsNumber = contrabandGoodsNumber;
	}

	public String getContrabandGoodsName() {
		return this.contrabandGoodsName;
	}

	public void setContrabandGoodsName(String contrabandGoodsName) {
		this.contrabandGoodsName = contrabandGoodsName;
	}

	public String getContrabandGoodsNorms() {
		return this.contrabandGoodsNorms;
	}

	public void setContrabandGoodsNorms(String contrabandGoodsNorms) {
		this.contrabandGoodsNorms = contrabandGoodsNorms;
	}

	public String getContrabandGoodsQuantity() {
		return this.contrabandGoodsQuantity;
	}

	public void setContrabandGoodsQuantity(String contrabandGoodsQuantity) {
		this.contrabandGoodsQuantity = contrabandGoodsQuantity;
	}

	public String getContrabandGoodsFeature() {
		return this.contrabandGoodsFeature;
	}

	public void setContrabandGoodsFeature(String contrabandGoodsFeature) {
		this.contrabandGoodsFeature = contrabandGoodsFeature;
	}

	public String getContrabandGoodsRemark() {
		return this.contrabandGoodsRemark;
	}

	public void setContrabandGoodsRemark(String contrabandGoodsRemark) {
		this.contrabandGoodsRemark = contrabandGoodsRemark;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}

	public Date getAddTime() {
		return addTime;
	}

	public void setAddTime(Date addTime) {
		this.addTime = addTime;
	}

	public Date getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}

	public String getPrisonCode() {
		return prisonCode;
	}

	public void setPrisonCode(String prisonCode) {
		this.prisonCode = prisonCode;
	}
}