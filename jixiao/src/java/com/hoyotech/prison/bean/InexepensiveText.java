package com.hoyotech.prison.bean;

import java.util.Date;

public class InexepensiveText {
	
	private String text_id;          //考学ID
	private JX_User u;                  //用户id
	private String text_grade;       //考学成绩
	private Date text_time;        //考学时间
	
	public String getText_id() {
		return text_id;
	}
	public void setText_id(String textId) {
		text_id = textId;
	}
	public JX_User getU() {
		return u;
	}
	public void setU(JX_User u) {
		this.u = u;
	}
	public String getText_grade() {
		return text_grade;
	}
	public void setText_grade(String textGrade) {
		text_grade = textGrade;
	}
	public Date getText_time() {
		return text_time;
	}
	public void setText_time(Date textTime) {
		text_time = textTime;
	}
	

}
