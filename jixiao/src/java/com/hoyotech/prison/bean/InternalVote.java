package com.hoyotech.prison.bean;

import java.util.Date;

public class InternalVote {
	private String internalVoteId;
	private String peopleId;
	private String departmentId;
	private String userId;
	private Integer votesNumber;// 票数
	private String remark;// 备注
	private Date voteTime;// 投票时间
	private String voteType;// 投票类型 "1":内部 "0":公众
	private String tel;
	private String name;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getTel() {
		return tel;
	}

	public void setTel(String tel) {
		this.tel = tel;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getInternalVoteId() {
		return internalVoteId;
	}

	public void setInternalVoteId(String internalVoteId) {
		this.internalVoteId = internalVoteId;
	}

	public String getPeopleId() {
		return peopleId;
	}

	public void setPeopleId(String peopleId) {
		this.peopleId = peopleId;
	}

	public String getDepartmentId() {
		return departmentId;
	}

	public void setDepartmentId(String departmentId) {
		this.departmentId = departmentId;
	}

	public Integer getVotesNumber() {
		return votesNumber;
	}

	public void setVotesNumber(Integer votesNumber) {
		this.votesNumber = votesNumber;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}

	public Date getVoteTime() {
		return voteTime;
	}

	public void setVoteTime(Date voteTime) {
		this.voteTime = voteTime;
	}

	public String getVoteType() {
		return voteType;
	}

	public void setVoteType(String voteType) {
		this.voteType = voteType;
	}

}
