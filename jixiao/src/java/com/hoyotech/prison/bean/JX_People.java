package com.hoyotech.prison.bean;

import java.util.Date;

import com.hoyotech.prison.log.PropertyName;

public class JX_People {

	@PropertyName(name = "ID")
	private String id;
	@PropertyName(name = "姓名")
	private String name;
	@PropertyName(name = "职位")
	private PostTable posttable;
	@PropertyName(name = "所属部门")
	private JX_Department jx_department;
	@PropertyName(name = "创建时间")
	private Date add_date;
	@PropertyName(name = "工作职责")
	private String duty;

	public JX_People() {

	}

	public String getId() {
		return id;
	}




	public void setId(String id) {
		this.id = id;
	}




	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public PostTable getPosttable() {
		return posttable;
	}

	public void setPosttable(PostTable posttable) {
		this.posttable = posttable;
	}

	public JX_Department getJx_department() {
		return jx_department;
	}

	public void setJx_department(JX_Department jxDepartment) {
		jx_department = jxDepartment;
	}

	public Date getAdd_date() {
		return add_date;
	}

	public void setAdd_date(Date addDate) {
		add_date = addDate;
	}

	public String getDuty() {
		return duty;
	}

	public void setDuty(String duty) {
		this.duty = duty;
	}

}