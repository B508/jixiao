package com.hoyotech.prison.bean;

import com.hoyotech.prison.log.PropertyName;

public class RolePermission {

	@PropertyName(name = "ID")
	private String id;
	private String roleId;
	private Permission perId;
	private int isSync = 0;// 是否同步；0：未同步；1：已同步

	public RolePermission() {

	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getRoleId() {
		return roleId;
	}

	public void setRoleId(String roleId) {
		this.roleId = roleId;
	}

	public Permission getPerId() {
		return perId;
	}

	public void setPerId(Permission perId) {
		this.perId = perId;
	}

	public int getIsSync() {
		return isSync;
	}

	public void setIsSync(int isSync) {
		this.isSync = isSync;
	}

}
