package com.hoyotech.prison.bean;

import java.util.Date;

import com.hoyotech.prison.log.PropertyName;

public class SendExamine {

	@PropertyName(name = "ID")
	private String id;
	@PropertyName(name = "被拘留人")
	private Prisoner prisoner;//被拘留人
	@PropertyName(name = "拘送人")
	private String sender;//拘送人
	@PropertyName(name = "拘送人证件号码")
	private String senderNumber;//拘送人证件号码
	@PropertyName(name = "拘送人证件号码")
	private String senderPhone;//拘送人联系方式
	@PropertyName(name = "寄押原因")
	private String sendReason;//寄压原因
	@PropertyName(name = "寄押时间")
	private Date sendDate;//寄压时间
	@PropertyName(name = "所负责人")
	private String leader;//所主管公安机关负责人
	@PropertyName(name = "所主管审批意见")
	private String leaderExamine;//所主管公安机关负责人审批意见
	@PropertyName(name = "审批时间")
	private Date leaderExamineDate;//所主管公安机关负责人审批时间
	@PropertyName(name = "备注")
	private String remark;//备注
	private String examine="0";
	private int state=1;
	private Date addTime;
	private Date updateTime;
	private String prisonCode;
	
	private int isSync = 0;// 是否同步；0：未同步；1：已同步
	
	SendExamine(){
	}
	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public int getIsSync() {
		return isSync;
	}

	public void setIsSync(int isSync) {
		this.isSync = isSync;
	}
	public String getExamine() {
		return examine;
	}

	public void setExamine(String examine) {
		this.examine = examine;
	}

	public Prisoner getPrisoner() {
		return prisoner;
	}
	public void setPrisoner(Prisoner prisoner) {
		this.prisoner = prisoner;
	}
	public String getSender() {
		return sender;
	}
	public void setSender(String sender) {
		this.sender = sender;
	}
	public String getSenderNumber() {
		return senderNumber;
	}
	public void setSenderNumber(String senderNumber) {
		this.senderNumber = senderNumber;
	}
	public String getSenderPhone() {
		return senderPhone;
	}
	public void setSenderPhone(String senderPhone) {
		this.senderPhone = senderPhone;
	}
	public String getSendReason() {
		return sendReason;
	}
	public void setSendReason(String sendReason) {
		this.sendReason = sendReason;
	}
	public Date getSendDate() {
		return sendDate;
	}
	public void setSendDate(Date sendDate) {
		this.sendDate = sendDate;
	}
	public String getLeader() {
		return leader;
	}
	public void setLeader(String leader) {
		this.leader = leader;
	}
	public String getLeaderExamine() {
		return leaderExamine;
	}
	public void setLeaderExamine(String leaderExamine) {
		this.leaderExamine = leaderExamine;
	}
	public Date getLeaderExamineDate() {
		return leaderExamineDate;
	}
	public void setLeaderExamineDate(Date leaderExamineDate) {
		this.leaderExamineDate = leaderExamineDate;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	public Date getAddTime() {
		return addTime;
	}
	public void setAddTime(Date addTime) {
		this.addTime = addTime;
	}
	public Date getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}
	public String getPrisonCode() {
		return prisonCode;
	}
	public void setPrisonCode(String prisonCode) {
		this.prisonCode = prisonCode;
	}
}
