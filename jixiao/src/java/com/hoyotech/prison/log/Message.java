package com.hoyotech.prison.log;

import java.util.Date;

public class Message {
	private String id;
	@PropertyName(name = "时间")
	private Date time;
	@PropertyName(name = "用户名")
	private String userName;
	@PropertyName(name = "真实姓名")
	private String realName;
	@PropertyName(name = "模块ID")
	private String moduleId;
	@PropertyName(name = "模块名")
	private String moduleName;
	@PropertyName(name = "操作")
	private String operate;
	@PropertyName(name = "操作类型")
	private int operateId;
	@PropertyName(name = "IP")
	private String ip;
	@PropertyName(name = "地址")
	private String address;
	@PropertyName(name = "状态")
	private int state = 1; // 状态
	private int sysMark = 0; // 系统标识，0：拘留所，1：省厅
	private String prisonCode;
	private int isSync = 0;// 是否同步；0：未同步；1：已同步

	public Message() {

	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public Date getTime() {
		return time;
	}

	public void setTime(Date time) {
		this.time = time;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getRealName() {
		return realName;
	}

	public void setRealName(String realName) {
		this.realName = realName;
	}

	public String getModuleId() {
		return moduleId;
	}

	public void setModuleId(String moduleId) {
		this.moduleId = moduleId;
	}

	public String getModuleName() {
		return moduleName;
	}

	public void setModuleName(String moduleName) {
		this.moduleName = moduleName;
	}

	public String getOperate() {
		return operate;
	}

	public void setOperate(String operate) {
		this.operate = operate;
	}

	public int getOperateId() {
		return operateId;
	}

	public void setOperateId(int operateId) {
		this.operateId = operateId;
	}

	public String getIp() {
		return ip;
	}

	public void setIp(String ip) {
		this.ip = ip;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}

	public int getSysMark() {
		return sysMark;
	}

	public void setSysMark(int sysMark) {
		this.sysMark = sysMark;
	}

	public String getPrisonCode() {
		return prisonCode;
	}

	public void setPrisonCode(String prisonCode) {
		this.prisonCode = prisonCode;
	}

	public int getIsSync() {
		return isSync;
	}

	public void setIsSync(int isSync) {
		this.isSync = isSync;
	}

}
